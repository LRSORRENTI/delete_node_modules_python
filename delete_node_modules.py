import os
import shutil

def delete_node_modules(directory):
    # Walk through the directory
    for root, dirs, files in os.walk(directory):
        if 'node_modules' in dirs:
            # Construct the full path to the node_modules directory
            nm_path = os.path.join(root, 'node_modules')
            try:
                # Remove the directory
                shutil.rmtree(nm_path)
                print(f"Deleted {nm_path}")
            except Exception as e:
                # If there is an error, print it and skip this directory
                print(f"Error deleting {nm_path}: {e}")

# Specify the path to your Desktop or any other directory
base_directory = "C:\\Users\\username\\Desktop"
delete_node_modules(base_directory)
